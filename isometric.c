#include "fdf.h"

void	isometric(int *x, int *y, int z, t_fdf *data)
{
	int	a;

	a = *x;
	*x = ((*x - *y) * cos(data->rotate_x));
	*y = (((a + *y) * sin(data->rotate_y)) - z);
}
